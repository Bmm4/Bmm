  tdrStyle->SetTitleBorderSize(0);
  tdrStyle->SetTitleFillColor(1000);
  tdrStyle->SetOptTitle(1);    // 0=No Title
  tdrStyle->SetTitleH(0.04); // Set the height of the title box

